import { ethers } from "./ethers.js";

let signer = null;
let contract = null;
let supply = 0;

// called when you click on element #id
const connect = async () => {
  const provider = new ethers.providers.Web3Provider(window.ethereum, "any");
  await provider.send("eth_requestAccounts", []);
  signer = provider.getSigner();
  contract = new ethers.Contract(
    "0x0db9e2f4395a179e4add26b43cfe8e4ea1830b46",
    contractAbi,
    signer
  );
  supply = await contract.totalSupply();
  $("#balance").text(
    "You have " +
      (await contract.balanceOf(await signer.getAddress())) +
      " miladys"
  );
  $("#price").text(
    "Current Price: " + (await contract.getPrice()) / 10 ** 18 + " ETH"
  );
  $("#supply").text("Minted Supply: " + supply + "/1000");
  $("#connect").text((await signer.getAddress()).substr(0, 6) + "...");
};

$("#connect").click(function () {
  connect();
});

$("#tweet").click(async function () {
  if (signer) {
    const timestamp = Date.now();
    const text = $("textarea").val();

    // https://docs.ethers.io/v5/api/signer/#Signer-signMessage
    const signature = await signer.signMessage(
      "MiladyMumble: " + timestamp.toString() + " " + text
    );

    //verifies thae signature, and that person who made the signature
    // has the correctnft in thgeir wallet

    // then, if it's valid, posts to twitter
    $.ajax("https://dev.miladymumbleapi.com/tweets", {
      data: JSON.stringify({
        timestamp,
        signature,
        text,
      }),
      contentType: "application/json",
      type: "POST",
    }).then((r) => alert(r.message));
  } else {
    alert("Connect wallet first");
  }
});
